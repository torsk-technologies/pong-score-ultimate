    val definitions: String = """
            2.5.1 
           A rally is the period during which the ball is in play. 
            
            2.5.2 
            The ball is in play from the last moment at which it is stationary on the palm of the free hand before being intentionally projected in service until the rally is decided as a let or a point.
            
            2.5.3 
            A let is a rally of which the result is not scored.
            
            2.5.4 
            A point is a rally of which the result is scored.
            
            2.5.5 
            The racket hand is the hand carrying the racket.
            
            2.5.6 
            The free hand is the hand not carrying the racket; the free arm is the arm of the free hand.
            
            2.5.7 
            A player strikes the ball if he or she touches it in play with his or her racket, held in the hand, or with his or her racket hand below the wrist.
            
            2.5.8 
            A player obstructs the ball if he or she, or anything he or she wears or carries, touches it in play when it is above or travelling towards the playing surface, not having touched his or her court since last being struck by his or her opponent.
            
            2.5.9 
            The server is the player due to strike the ball first in a rally.
            
            2.5.10 
            The receiver is the player due to strike the ball second in a rally.
            
            2.5.11 
            The umpire is the person appointed to control a match.
            
            2.5.12 
            The assistant umpire is the person appointed to assist the umpire with certain decisions.
            
            2.5.13 
            Anything that a player wears or carries includes anything that he or she was wearing or carrying, other than the ball, at the start of the rally.
            
            2.5.14 
            The end line shall be regarded as extending indefinitely in both directions.
        """.trimIndent()

    val service: String = """
        2.6.1 
        Service shall start with the ball resting freely on the open palm of the server's stationary free hand.
        
        2.6.2 
        The server shall then project the ball near vertically upwards, without imparting spin, so that it rises at least 16cm after leaving the palm of the free hand and then falls without touching anything before being struck.
        
        2.6.3 
        As the ball is falling the server shall strike it so that it touches first his or her court and then touches directly the receiver's court; in doubles, the ball shall touch successively the right half court of server and receiver.
        
        2.6.4 
        From the start of service until it is struck, the ball shall be above the level of the playing surface and behind the server's end line, and it shall not be hidden from the receiver by the server or his or her doubles partner or by anything they wear or carry.
        
        2.6.5 
        As soon as the ball has been projected, the server’s free arm and hand shall be removed from the space between the ball and the net.
        The space between the ball and the net is defined by the ball, the net and its indefinite upward extension.
        
        2.6.6 
        It is the responsibility of the player to serve so that the umpire or the assistant umpire can be satisfied that he or she complies with the requirements of the Laws, and either may decide that a service is incorrect.
        
        2.6.6.1
        If either the umpire or the assistant umpire is not sure about the legality of a service he or she may, on the first occasion in a match, interrupt play and warn the server; but any subsequent service by that player or his or her doubles partner which is not clearly legal shall be considered incorrect.
        
        2.6.7 
        Exceptionally, the umpire may relax the requirements for a correct service where he or she is satisfied that compliance is prevented by physical disability.
    """.trimIndent()
